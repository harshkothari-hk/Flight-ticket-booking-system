<h1>About us...</h1>

<p>Skyway is the flag carrier airline of India and the largest airline in India in terms of passengers carried, after IndiGo and Jet Airways. </p>


<p>It is owned by Air India Limited, a Government of India enterprise and operates a fleet of Airbus and Boeing aircraft serving various domestic and international destinations. It is headquartered in New Delhi. Skyway has major domestic hubs at Indira Gandhi International Airport, New Delhi and Chhatrapati Shivaji International Airport, Mumbai and secondary hubs at Netaji Subhas Chandra Bose International Airport, Kolkata and Chennai International Airport. Air India became the 27th member of Star Alliance on 11 July 2014.</p>

<p>Founded in 1932 by J. R. D. Tata and later renamed to Tata Airlines, he flew its first single-engine De Havilland Puss Moth, carrying air mail from Karachi to Bombay, and later continuing to Madras. After World War II, it became a public limited company and was renamed to Air-India.</p>

<p>On 21 February 1960, it included it first Boeing 707–420 named Gauri Shankar and became the first Asian airline to induct a jet aircraft in its fleet. In 2000–01, attempts were made to privatise Air India and in 2006 onwards, it suffered losses after its merger with Indian Airlines.</p>

